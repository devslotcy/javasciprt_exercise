//  Mantıksal operatörler ve Karşılaştırmalar
//  && -> Karşılaştırmalardan 1 tanesi false ise false değeri dönecektir.
//  || -> Karşılaştırılan ifadelerden 1 tanesi bile true ise true değeri dönecektir.
 
 
 
 let sayi1 = 10;
 let sayi2 = 9;
 
 console.log( (sayi1 > 5)  &&  (sayi2 < 5) );
 console.log( (sayi1 < 15) ||  (sayi2 > 11) );


 